#include "builtins.h"

/**
 * @brief Checks if string consists only of 'n' characters.
 * @param str Input string.
 * @return 1 if only 'n', 0 otherwise.
 */
static int	all_n(const char *str)
{
	int	i;

	if (!str || str[0] != '-')
		return (0);
	i = 1;
	if (!str[i])
		return (0);
	while (str[i])
	{
		if (str[i] != 'n')
			return (0);
		i++;
	}
	return (i > 1);
}

/**
 * @brief Implements echo builtin with '-n' option and variable expansion
 * @return 0 on success, 1 on failure.
 */
int	builtin_echo(char **argv)
{
	int	i;
	int	no_newline;

	i = 1;
	no_newline = 0;
	while (argv[i] && all_n(argv[i]))
	{
		no_newline = 1;
		i++;
	}
	while (argv[i])
	{
		write(1, argv[i], ft_strlen(argv[i]));
		if (argv[i + 1])
			write(1, " ", 1);
		i++;
	}
	if (!no_newline)
		write(1, "\n", 1);
	return (0);
}
